package day1;

public class JavaDatatypes {
    //main method
    public static void main(String[] args){

        //Primitive Datatypes ,dispalyed in blue colour
        //Syntax: Datatype  variable =VALUE
        int a=20;
        int b=30;
        long aadharcard=123456789987642331l;//suffix l
        float price=99.65003f;//suffix f
        double rate=122332332.54698;
        char ch='&';//store only one single character
        short sh=1234;//can only store upto 4 no.s
        boolean bool=false;// store boolean values true or false

        //String is a built in class,not a datatype,its displayed in black colour
        String name="sonal_anup_gadve_131719";


        System.out.println(name);
        System.out.println(bool);
        System.out.println(sh);
        System.out.println(ch);
        System.out.println(aadharcard);
        System.out.println(price);
        System.out.println(rate);
    }
}
