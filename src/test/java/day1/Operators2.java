package day1;

public class Operators2 {
    public static void main(String[] args) {
        int x=10;
        x++;
        System.out.println(x);
        int y=10;
        y--;
        System.out.println(y);
        System.out.println("************************");
        for(int i=0;i<10;i++)
            System.out.println(i);
        System.out.println("************************");
        for (int i=10;i>=1;i--)
            System.out.println(i);


    }
}
