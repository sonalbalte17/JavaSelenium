package UdemyPractice;

import java.awt.*;

public class ForLoop {
    public static void main(String[] args) {
        //print 1 to 10
        int i=1;
       int n=15;
        for (i=1;i<=10;i=i+2)
        {
            System.out.println(i);
        }
        for (n=15;n>=1;n=n-2)
        {
            System.out.println(n);
        }
    }
}
